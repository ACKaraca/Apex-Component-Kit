export class PerformanceMonitor {
  private metrics: Map<string, number[]> = new Map();

  /**
   * Metrik kaydet
   */
  recordMetric(name: string, value: number): void {
    if (!this.metrics.has(name)) {
      this.metrics.set(name, []);
    }
    this.metrics.get(name)!.push(value);
  }

  /**
   * Ortalama metrik al
   */
  getAverageMetric(name: string): number {
    const values = this.metrics.get(name);
    if (!values || values.length === 0) return 0;
    return values.reduce((sum, val) => sum + val, 0) / values.length;
  }

  /**
   * Metrik istatistiklerini al
   */
  getMetricStats(name: string): {
    count: number;
    average: number;
    min: number;
    max: number;
  } {
    const values = this.metrics.get(name);
    if (!values || values.length === 0) {
      return { count: 0, average: 0, min: 0, max: 0 };
    }

    return {
      count: values.length,
      average: this.getAverageMetric(name),
      min: Math.min(...values),
      max: Math.max(...values)
    };
  }

  /**
   * TÃ¼m metrikleri al
   */
  getAllMetrics(): Record<string, number[]> {
    const result: Record<string, number[]> = {};
    this.metrics.forEach((values, name) => {
      result[name] = [...values];
    });
    return result;
  }

  /**
   * Performance report oluÅŸtur
   */
  generateReport(): string {
    const report: string[] = [];
    report.push('ðŸš€ ACK Performance Monitoring Report');
    report.push('====================================');

    this.metrics.forEach((values, name) => {
      const stats = this.getMetricStats(name);
      report.push(`\nðŸ“Š ${name}:`);
      report.push(`  Calls: ${stats.count}`);
      report.push(`  Average: ${stats.average.toFixed(2)}ms`);
      report.push(`  Min: ${stats.min.toFixed(2)}ms`);
      report.push(`  Max: ${stats.max.toFixed(2)}ms`);
    });

    return report.join('\n');
  }
}

